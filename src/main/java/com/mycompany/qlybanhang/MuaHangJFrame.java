    private void loadData() {
        tableModel.setColumnIdentifiers(new Object[]{"Tên Sản Phẩm", "Loại Sản Phẩm", "Giá tiền", "Số Lượng"});
        tableModel.setRowCount(0);

        try (Connection connection = getConnection();
             Statement statement = connection.createStatement();
             ResultSet resultSet = statement.executeQuery("SELECT * FROM SanPham")) {

            while (resultSet.next()) {
                Object[] row = {
                    resultSet.getString("ProductName"),
                    resultSet.getString("LoaiSanPham"), // Sửa thứ tự
                    resultSet.getDouble("Price"),
                    resultSet.getInt("StockQuantity")
                };
                tableModel.addRow(row);
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }  

