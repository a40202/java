    private void themVaoGioHang() {
    int selectedRow = tableMuaHang.getSelectedRow();

    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Vui lòng chọn một sản phẩm để thêm vào giỏ hàng!");
        return;
    }

    Object tenSanPhamObj = tableModel.getValueAt(selectedRow, 0);
    Object loaiSanPhamObj = tableModel.getValueAt(selectedRow, 1);
    Object giaTienObj = tableModel.getValueAt(selectedRow, 2);
    Object soLuongObj = tableModel.getValueAt(selectedRow, 3);

    // Kiểm tra null trước khi sử dụng
    if (tenSanPhamObj == null || loaiSanPhamObj == null || giaTienObj == null || soLuongObj == null) {
        JOptionPane.showMessageDialog(this, "Dữ liệu sản phẩm không hợp lệ!");
        return;
    }

    String tenSanPham = tenSanPhamObj.toString();
    String loaiSanPham = loaiSanPhamObj.toString();
    double giaTien = (giaTienObj instanceof Number) ? ((Number) giaTienObj).doubleValue() : Double.parseDouble(giaTienObj.toString());
    int soLuong = (soLuongObj instanceof Number) ? ((Number) soLuongObj).intValue() : Integer.parseInt(soLuongObj.toString());

    if (soLuong <= 0) {
        JOptionPane.showMessageDialog(this, "Sản phẩm này đã hết hàng!");
        return;
    }

    tableMuaHang.setValueAt(soLuong - 1, selectedRow, 3);

    DefaultTableModel modelGioHang = (DefaultTableModel) tableGioHang.getModel();
    boolean daTonTai = false;

    for (int i = 0; i < modelGioHang.getRowCount(); i++) {
        Object existingProduct = modelGioHang.getValueAt(i, 0);

        // Kiểm tra null trước khi gọi equals()
        if (existingProduct != null && existingProduct.equals(tenSanPham)) {
            int soLuongCu = (int) modelGioHang.getValueAt(i, 2);
            modelGioHang.setValueAt(soLuongCu + 1, i, 2);
            modelGioHang.setValueAt((soLuongCu + 1) * giaTien, i, 3);
            daTonTai = true;
            break;
        }
    }

    if (!daTonTai) {
        modelGioHang.addRow(new Object[]{tenSanPham, loaiSanPham, 1, giaTien});
    }
}
